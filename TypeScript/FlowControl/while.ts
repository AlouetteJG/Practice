//Iterative = [while, for]

var n:number = 5;
var i=0;
var m:number = 5;
var j=0;
var o:number = 5;
var k=0;

while(i<n){
    console.log(i); //Esto imprime el numero y despues sigue el proceso
    i++;
}

while(j<m){
    console.log(++j); //Esto es para imprimir el numero despues de un incremento
}

while(k<o){
    console.log(k++); //Esto es para imprimir el numero antes del incremento
}